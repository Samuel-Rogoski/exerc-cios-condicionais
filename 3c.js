let dia_da_semana = 3;

if (dia_da_semana <= 0 || dia_da_semana > 7) {
    console.log("Dia inválido da semana");
} else if (dia_da_semana == 1) {
    console.log("Domingo");
} else if (dia_da_semana == 2) {
    console.log("Segunda-feira");
} else if (dia_da_semana == 3) {
    console.log("Terça-feira");
} else if (dia_da_semana == 4) {
    console.log("Quarta-feira");
} else if (dia_da_semana == 5) {
    console.log("Quinta-feira");
} else if (dia_da_semana == 6) {
    console.log("Sexta-feira");
} else if (dia_da_semana == 7) {
    console.log("Sábado");
}